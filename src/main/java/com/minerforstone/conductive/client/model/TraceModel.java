import net.minecraft.resources.ResourceLocation;
import net.minecraftforge.api.distmarker.Dist;
import net.minecraftforge.api.distmarker.OnlyIn;

@OnlyIn(Dist.CLIENT)
public class TraceModel extends Model {
	// This layer location should be baked with EntityRendererProvider.Context in the entity renderer and passed into this model's constructor
	public static final ModelLayerLocation LAYER_LOCATION = new ModelLayerLocation(new ResourceLocation("conductive", "trace"), "main");
	private final ModelPart bit;

	public TraceModel(ModelPart root) {
		super(RenderType::entitySolid);
		this.bit = root.getChild("bit");
	}

	public static LayerDefinition createBodyLayer() {
		MeshDefinition meshdefinition = new MeshDefinition();
		PartDefinition partdefinition = meshdefinition.getRoot();

		partdefinition.addOrReplaceChild("bit", CubeListBuilder.create().texOffs(0, 0).addBox(0.0F, 0.01F, 0.0F, 1.0F, 0.0F, 1.0F), PartPose.ZERO);

		return LayerDefinition.create(meshdefinition, 16, 16);
	}

	@Override
	public void renderToBuffer(PoseStack poseStack, VertexConsumer vertexConsumer, int packedLight, int packedOverlay, float red, float green, float blue, float alpha) {
		bit.render(poseStack, vertexConsumer, packedLight, packedOverlay, red, green, blue, alpha);
	}
}